class Orden:

    def set_name(self, name):
        self.__name = name

    def get_name(self):
        return self.__name

    def set_nit(self, nit):
        self.__nit = nit

    def get_nit(self):
        return self.__nit

    def set_address(self, address):
        self.__address = address

    def get_address(self):
        return self.__address

    def set_tip(self, tip):
        self.__tip = tip

    def get_tip(self):
        return self.__tip

    def set_item(self, items):
        self.__items = items

    def get_items(self):
        return self.__items